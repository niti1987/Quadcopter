//********************************************************************************
//********************************************************************************
//********************************************************************************
/// An interface for classes that solve constraints for physical objects.
class Constraint
{
	public:
		
		//********************************************************************************
		//********************************************************************************
		//********************************************************************************
		//******	Destructor
			
			
			
			
			/// Destroy this constraint.
			virtual ~Constraint()
			{
			}
			
			
			
			
		//********************************************************************************
		//********************************************************************************
		//********************************************************************************
		//******	Constraint Solving Methods
			
			
			
			
			/// Solve all instances of this constraint for the specified time step and number of solver iterations.
			virtual void solve( Real dt, Size numIterations ) = 0;
			
			
			
};




